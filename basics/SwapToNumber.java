package com.tmf.basics;
public class SwapToNumber {
    
	public static void main(String[] args) {
		int a=2;
	     int b=3;
	     int c = 0;
	     System.out.println("the number before swaping:"+a);
	     System.out.println("the number before swaping:"+b);
	     
	     c=a;
	     c=b;
	     b=c;
	     System.out.println("the number after swapping:"+a);
	     System.out.println("the number after swapping:"+b);
			

	}

}
